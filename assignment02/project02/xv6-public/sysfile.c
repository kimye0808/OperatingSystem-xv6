int
sys_exec2(void)
{
  char *path, *argv[MAXARG];
  int stackSize;
  int i;
  uint uargv, uarg;

  if(argstr(0, &path) < 0 || argint(1, (int*)&uargv) < 0 || argint(2, &stackSize) < 0){
    return -1;
  }
  memset(argv, 0, sizeof(argv));
  for(i=0;; i++){
    if(i >= NELEM(argv))
      return -1;
    if(fetchint(uargv+4*i, (int*)&uarg) < 0)
      return -1;
    if(uarg == 0){
      argv[i] = 0;
      break;
    }
    if(fetchstr(uarg, &argv[i]) < 0)
      return -1;
  }
  return exec2(path, argv, stackSize);
}

/*이 함수는 Unix 파이프(pipe)를 만들고, 파이프의 읽기(rf)와 쓰기(wf)를 위한 파일 구조체(file struct)를 할당합니다.
우선, 함수의 인자로 들어오는 fd 배열은 파이프의 파일 디스크립터(file descriptor)를 저장할 배열입니다. 이 배열에는 
파이프 읽기용과 쓰기용 두 개의 파일 디스크립터가 저장됩니다. 그리고 함수는 pipealloc 함수를 호출하여, 파이프 읽기용과 쓰기용 파일 구조체를 할당받습니다. 
pipealloc 함수는 파이프의 읽기와 쓰기를 위한 파일 구조체를 생성하고, 그것들을 rf와 wf에 각각 할당해주는 역할을 합니다.
다음으로, 파일 디스크립터 할당을 위해 fdalloc 함수를 호출합니다. 이 함수는 현재 프로세스의 파일 테이블에서 빈 파일 디스크립터를 찾아 할당해주는 역할을 합니다.
이 과정에서, 파이프 읽기용 파일 구조체를 할당한 fd0과 쓰기용 파일 구조체를 할당한 fd1이 반환됩니다. 그러나, 만약 파일 디스크립터 할당에 실패하면, 
이미 할당된 파일 디스크립터를 해제하고 파일 구조체도 닫은 뒤, 함수는 -1을 반환합니다. 파일 디스크립터 할당에 성공했다면, 
파이프 읽기용 파일 디스크립터를 fd[0]에 저장하고, 쓰기용 파일 디스크립터를 fd[1]에 저장합니다.
마지막으로, 함수는 0을 반환하여 정상적으로 파이프 생성이 완료되었음을 알립니다.

파이프(Pipe)는 Unix 계열 운영체제에서 제공하는 기능으로, 프로세스 간에 데이터를 주고받기 위한 메커니즘입니다. 
파이프는 한 쪽에서 쓰기(write) 작업을 하면, 다른 쪽에서 읽기(read) 작업을 통해 데이터를 가져올 수 있습니다.
파이프는 일종의 큐(queue)로, 한 쪽에서 데이터를 쓰면 다른 쪽에서는 그 데이터를 읽을 수 있습니다. 파이프는 두 개의 파일 디스크립터를 가지며, 
하나는 파이프에 데이터를 쓰기 위한 쓰기용 파일 디스크립터(write descriptor), 
다른 하나는 파이프에서 데이터를 읽기 위한 읽기용 파일 디스크립터(read descriptor)입니다.
파이프는 일반적으로 한 프로세스가 다른 프로세스로 데이터를 전달하기 위해 사용됩니다. 
예를 들어, 한 프로세스에서 생성한 출력 결과를 다른 프로세스에서 이용하기 위해 파이프를 사용할 수 있습니다. 
또한, 파이프를 이용하여 여러 개의 프로세스를 연결하면 데이터를 순차적으로 전달할 수 있어 복잡한 작업을 수행하는데 도움을 줄 수 있습니다.
*/
int
sys_pipe(void)
{
  int *fd;
  struct file *rf, *wf;
  int fd0, fd1;

  if(argptr(0, (void*)&fd, 2*sizeof(fd[0])) < 0)
    return -1;
  if(pipealloc(&rf, &wf) < 0)
    return -1;
  fd0 = -1;
  if((fd0 = fdalloc(rf)) < 0 || (fd1 = fdalloc(wf)) < 0){
    if(fd0 >= 0)
      myproc()->ofile[fd0] = 0;
    fileclose(rf);
    fileclose(wf);
    return -1;
  }
  fd[0] = fd0;
  fd[1] = fd1;
  return 0;
}
